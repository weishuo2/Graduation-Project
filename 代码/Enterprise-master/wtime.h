#ifndef __H_TIME__
#define __H_TIME__

#include <sys/time.h>
#include <stdlib.h>

inline double wtime()
{
	double time[2];	
	struct timeval time1;
	gettimeofday(&time1, NULL);//获取当前时间

	time[0]=time1.tv_sec;//秒
	time[1]=time1.tv_usec;//微秒

	return time[0]+time[1]*1.0e-6;
}

#endif
